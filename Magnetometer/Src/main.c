/**
  * @brief  Function implementing the lis3mdl_task thread.
  * @param  argument: Not used 
  * @retval None
  */
/* USER CODE END Header_LIS3MDLTask */
void LIS3MDLTask(void const * argument)
{
	osEvent evt;
	struct lis3mdl_dev lis3mdl;

	/* USER CODE BEGIN 5 */
	lis3mdl_init(&lis3mdl, &hi2c1);
	lis3mdl_set_output_data_rate(&lis3mdl, OUTPUT_RATE_10);
	lis3mdl_set_axis_mode(&lis3mdl, AXIS_HIGH_PERF, AXIS_HIGH_PERF);
	lis3mdl_set_full_scale(&lis3mdl, FS_12GAUSS);
	lis3mdl_set_system_mode(&lis3mdl, SYS_MODE_CONTINUOUS_CONVERSION);

	/* Infinite loop */
	for(;;)
	{
		evt = osSignalWait(0x01, osWaitForever);
		if(evt.status == osEventSignal)
		{
			lis3mdl_read_mag_axis(&lis3mdl, magnetic_data.magnetic_field);
			lis3mdl_read_temp(&lis3mdl, &(magnetic_data.temperature));
		}
	}
	/* USER CODE END 5 */
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	static int drdy_ticks = 0;

	if(GPIO_Pin == GPIO_PIN_7)
	{
		/* Count 10 ticks to get a 1 Hz reading rate from a 10 Hz update rate */
		drdy_ticks++;
		if(drdy_ticks == 10)
		{
			drdy_ticks = 0;
			osSignalSet(lis3mdl_taskHandle, 0x01);
		}
	}
}

